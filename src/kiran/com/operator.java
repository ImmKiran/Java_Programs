package kiran.com;

public class operator {
    public static void main(String[] args) {
        int a = 4;
        a *= 3;
        System.out.println(a);
        System.out.println(6 == 6);//comparison operator
        System.out.println(10 > 5 && 20 > 50);//in and operator if both are true then it print true else false
        System.out.println(20 > 30 || 30 > 10);//in OR operator if both false then it returns false else true
        // bitwise operator
        System.out.println(2 & 3);
      /*  2 = 10
        3 = 11
    ..................
           1 0 by using and operator of above two */
    }
}
